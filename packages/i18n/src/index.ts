/**
 * Detect the best supported locale from a BCP-47 tag (e.g. from `navigator.language`).
 * Matches by exact tag, then by primary subtag. Returns `null` if no supported locale matches.
 */
export function matchSupportedLocale(tag: string | null | undefined): Locale | null {
  if (!tag) return null;
  const normalized = tag.trim();
  if (!normalized) return null;
  const exact = (SUPPORTED_LOCALES as readonly string[]).find(
    (l) => l.toLowerCase() === normalized.toLowerCase(),
  );
  if (exact) return exact as Locale;
  const primary = normalized.split(/[-_]/)[0]!.toLowerCase();
  // Brazilian Portuguese gets priority when the user reports pt-BR / pt-br.
  if (normalized.toLowerCase() === 'pt-br') return 'pt-BR';
  const matched = (SUPPORTED_LOCALES as readonly string[]).find(
    (l) => l.toLowerCase().split(/[-_]/)[0] === primary,
  );
  return (matched as Locale | undefined) ?? null;
}
